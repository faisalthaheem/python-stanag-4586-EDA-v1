def test_decode_message20020():
    msg20020 = Message20020(PACKET_TO_DECODE)

    assert msg20020.time_stamp == 0x00
    assert msg20020.vehicle_id == 0x50
    assert msg20020.cucs_id == 0xA0
    assert msg20020.station_number == 0x01
    assert msg20020.requested_query_type == Message20010.QUERY_TYPE_SEND_CONFIG
    assert msg20020.get_response().strip() == "faisal"

def test_encode_message20020():
    msg20020 = Message20020(Message20020.MSGNULL)
    
    msg20020.time_stamp = 0x00
    msg20020.vehicle_id = 0x50
    msg20020.cucs_id = 0xA0
    msg20020.station_number = 0x01
    msg20020.requested_query_type = Message20010.QUERY_TYPE_SEND_CONFIG
    msg20020.set_response("faisal")

    assert msg20020.encode() == PACKET_TO_DECODE